public static function clean($string) {
   $string = str_replace(' ', '-', $string); // Replaces all spaces with hyphens.
   $string = preg_replace('/[^A-Za-z0-9\-]/', '', $string); // Removes special chars.
   return strtolower($string);
}
public static function uploadImageBase64($imageBase64,$imageName,$path){
        if (!is_dir(public_path().'/images/'.$path)) {
            mkdir(public_path().'/images/'.$path, 0777, true);
        }
        $png_url = $imageName."-".uniqid().".jpg";
        $path = public_path().'/images/'.$path.'/' .$png_url;
        Image::make(file_get_contents($imageBase64))->save($path);
        return $png_url;
}
public static function replace_dashes($string) {
    $string = strtolower(str_replace(" ", "-", $string));
    return $string;
}
public static function asset($arg){
    if(Request::secure()):
      return secure_asset($arg);
    else:
      return asset($arg);
    endif;
  }

